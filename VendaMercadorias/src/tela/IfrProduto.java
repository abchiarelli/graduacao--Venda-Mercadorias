    private void salvar() {
        Produto produto = criarProduto();

        if (produtoSelecionado == null) {
            if (new ProdutoDAO().salvar(produto) == null) {
                JOptionPane.showMessageDialog(this, Formatacao.mensagemSalvarSucess(this.titulo));
                limparRegistro();
                popularTabela();
                TbpPrincipal.setSelectedIndex(0);
            } else {
                JOptionPane.showMessageDialog(this, Formatacao.mensagemSalvarError(this.titulo));
            }
        } else {
            if (new ProdutoDAO().atualizar(produto) == null) {
                JOptionPane.showMessageDialog(this, Formatacao.mensagemAtualizarSucess(this.titulo));
                limparRegistro();
                popularTabela();
                TbpPrincipal.setSelectedIndex(0);
            } else {
                JOptionPane.showMessageDialog(this, Formatacao.mensagemAtualizarError(this.titulo));
            }
        }

        popularTabela();
    }

    private Produto criarProduto() {
        String descricao = tfdDescricao.getText();
        double valor = Double.parseDouble(Formatacao.formatarCasasDecimais(Double.parseDouble(tfdValor.getText()), 2).replace(',', '.'));
        double quantidade = Double.parseDouble(Formatacao.formatarCasasDecimais(Double.parseDouble(tfdQuantidade.getText()), 3).replace(',', '.'));

        if (produtoSelecionado == null) {
            return new Produto(descricao, valor, quantidade);
        } else {
            return new Produto(produtoSelecionado.getId(), descricao, valor, quantidade);
        }
    }

    public void limparRegistro() {
        tfdDescricao.setText("");
        tfdQuantidade.setText("");
        tfdValor.setText("");
        
        tfdDescricao.setBackground(Formatacao.colorNeutral());
        tfdQuantidade.setBackground(Formatacao.colorNeutral());
        tfdValor.setBackground(Formatacao.colorNeutral());
        
        tfdDescricao.requestFocus();
    }

    public void popularTabela() {
        alterarBotoesEdicao(false);

        popularArrayProdutos();

        Object[] cabecalho = {
            "Descricao",
            "Valor",
            "Estoque"
        };

        DefaultTableModel model = new DefaultTableModel(cabecalho, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        for (Produto produto : produtos) {
            String descricao = produto.getDescricao();
            String valor = Formatacao.formatarCasasDecimais(produto.getValor(), 2).replace('.', ',');
            String quantidade = Formatacao.formatarCasasDecimais(produto.getQuantidade(), 3).replace('.', ',');

            Object[] row = {descricao, valor, quantidade};

            model.addRow(row);
        }

        TblListagem.setModel(model);

        TblListagem.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        TableColumn coluna = null;
        for (int i = 0; i < TblListagem.getColumnCount(); i++) {
            coluna = TblListagem.getColumnModel().getColumn(i);
            coluna.setResizable(false);

            switch (i) {
                case 0:
                    coluna.setMaxWidth(400);
                    break;
                case 1:
                    coluna.setMaxWidth(150);
                    break;
                case 2:
                    coluna.setMaxWidth(150);
                    break;
            }
        }
    }

    private void popularArrayProdutos() {
        ProdutoDAO produtoDAO = new ProdutoDAO();

        if (TxtFiltro.getText().length() > 0) {

            String dml = "SELECT * FROM produto "
                    + "WHERE descricao ILIKE '%" + TxtFiltro.getText() + "%' "
                    + "ORDER BY descricao";

            produtos = produtoDAO.consultar(dml);
        } else {

            produtos = produtoDAO.consultarTodos();
        }
    }

    private void limparTabela() {
        DefaultTableModel model = (DefaultTableModel) TblListagem.getModel();
        model.setRowCount(0);
    }

    private void alterarBotoes() {
        BtnBuscar.setEnabled(TbpPrincipal.getSelectedIndex() == 0);
        BtnSalvar.setEnabled(TbpPrincipal.getSelectedIndex() == 1);
    }

    private void alterarBotoesEdicao(boolean setTo) {
        BtnAtualizar.setEnabled(setTo);
        BtnExcluir.setEnabled(setTo);
    }

    private boolean verificarCampos() {
        Automatizar.nome(tfdDescricao, false);
        Automatizar.valorDecimal(tfdValor, false);
        Automatizar.valorQuantidade(tfdQuantidade, false);

        return (Automatizar.nome(tfdDescricao, false)
                || Automatizar.valorDecimal(tfdValor, false)
                || Automatizar.valorQuantidade(tfdQuantidade, false));
    }


