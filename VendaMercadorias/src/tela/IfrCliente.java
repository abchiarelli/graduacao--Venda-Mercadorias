    private void salvar() {
        Cliente cliente = criarCliente();

        ClienteDAO clienteDAO = new ClienteDAO();
        if (clienteSelecionado == null) {
            if (clienteDAO.salvar(cliente) == null) {
                limparRegistro();
                popularTabela();
                JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao acadastrar Cliente.");
            }
        } else {
            if (clienteDAO.atualizar(cliente) == null) {
                limparRegistro();
                popularTabela();
                JOptionPane.showMessageDialog(this, "Cliente alterado com sucesso!");
                clienteSelecionado = null;
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao alterar Cliente.");
            }
        }
    }

    private Cliente criarCliente() {
        String nome = tfdNome.getText();
        String email = tfdEmail.getText();
        String cpf = tffCPF.getText();
        String telefone = tffTelefone.getText();
        String logradouro = tfdLogradouro.getText();
        ComboItem item = (ComboItem) cbbCidade.getSelectedItem();
        int idCidade = item.getId();
        LocalDate nascimento = new Formatacao().stringToLocalDate(tffNascimento.getText());

        if (clienteSelecionado == null) {
            return new Cliente(nome, email, cpf, telefone, logradouro, idCidade, nascimento);

        } else {
            return new Cliente(clienteSelecionado.getId(), nome, email, cpf, telefone, logradouro, idCidade, nascimento);
        }
    }

