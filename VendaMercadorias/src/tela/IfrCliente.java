    private void popularTabela() {
        alterarBotoesUpdate(false);
        popularArrayClientes();

        Object[] cabecalho = {"Nome", "Telefone", "Nascimento", "Cidade", "Logradouro", "e-mail"};

        DefaultTableModel model = new DefaultTableModel(cabecalho, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        CidadeDAO cidadeDAO = new CidadeDAO();

        for (Cliente cliente : clientes) {
            String nome = cliente.getNome();
            String telefone = cliente.getTelefone();
            String cidade = cidadeDAO.consultarId(cliente.getCidade()).getNome();
            String logradouro = cliente.getLogradouro();
            String email = cliente.getEmail();

            String nascimento = new Formatacao().localDateToString(cliente.getNascimento());

            String[] row = {nome, telefone, nascimento, cidade, logradouro, email};

            model.addRow(row);
        }

        TblClientes.setModel(model);
        TblClientes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        TableColumn coluna = null;

        for (int i = 0; i < TblClientes.getColumnCount(); i++) {
            coluna = TblClientes.getColumnModel().getColumn(i);
            coluna.setResizable(false);

            switch (i) {
                case 0: //Nome
                    coluna.setMaxWidth(400);
                    break;
                case 1: //Telefone
                    coluna.setMaxWidth(150);
                    break;
                case 2: //Nascimento
                    coluna.setMaxWidth(80);
                    break;
                case 3: //Cidade
                    coluna.setMaxWidth(200);
                    break;
                case 4: //Logradouro
                    coluna.setMaxWidth(300);
                    break;
                case 5: //e-mail
                    coluna.setMaxWidth(200);
                    break;
            }
        }
    }

    private void popularArrayClientes() {
        ClienteDAO clienteDAO = new ClienteDAO();

        if (TxtFiltroNome.getText().length() > 0) {
            String DML = "SELECT * FROM cliente "
                    + "WHERE nome ILIKE '%" + TxtFiltroNome.getText() + "%'"
                    + "ORDER BY nome;";

            clientes = clienteDAO.consultar(DML);
        } else {
            clientes = clienteDAO.consultarTodos();
        }

    }

    private void alterarBotoesUpdate(boolean setTo) {
        BtnAtualizar.setEnabled(setTo);
        BtnExcluir.setEnabled(setTo);
    }

    private boolean verificarCampos() {
        Automatizar.cpf(tffCPF, false);
        Automatizar.data(tffNascimento, false);
        Automatizar.email(tfdEmail, true);
        Automatizar.nome(tfdNome, false);
        Automatizar.logradouro(tfdLogradouro, false);
        Automatizar.telefone(tffTelefone, false);
        Automatizar.comboBox(cbbCidade, false);

        if (Automatizar.cpf(tffCPF, false)
                || Automatizar.data(tffNascimento, false)
                || Automatizar.email(tfdEmail, true)
                || Automatizar.nome(tfdNome, false)
                || Automatizar.logradouro(tfdLogradouro, false)
                || Automatizar.telefone(tffTelefone, false)
                || Automatizar.comboBox(cbbCidade, false)) {
            return true;
        } else {
            return false;
        }
    }


